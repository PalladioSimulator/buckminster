package org.eclipse.buckminster.ui.dependency.visualizer;

import org.eclipse.buckminster.ui.dependency.visualizer.resources.ImageCache;
import org.eclipse.core.runtime.IStatus;
import org.eclipse.core.runtime.Status;
import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 */
public class Activator extends AbstractUIPlugin {

	// The plug-in ID
	public static final String PLUGIN_ID = "org.eclipse.buckminster.ui.dependency.visualizer"; //$NON-NLS-1$

	// The shared instance
	private static Activator plugin;

	/**
	 * Returns the shared instance
	 * 
	 * @return the shared instance
	 */
	public static Activator getDefault() {
		return plugin;
	}

	/**
	 * The constructor
	 */
	public Activator() {
	}

	public void log(int severity, String message, Throwable t) {
		IStatus status = new Status(severity, PLUGIN_ID, message, t);
		getLog().log(status);

	}

	public void log(int severity, Throwable t) {
		log(severity, t.getLocalizedMessage(), t);
	}

	public void log(Throwable t) {
		log(IStatus.ERROR, t);
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.eclipse.ui.plugin.AbstractUIPlugin#start(org.osgi.framework.BundleContext
	 * )
	 */
	@Override
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.eclipse.ui.plugin.AbstractUIPlugin#stop(org.osgi.framework.BundleContext
	 * )
	 */
	@Override
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		ImageCache.dispose();
		super.stop(context);
	}

}
